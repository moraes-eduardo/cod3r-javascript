// laço for - repetição

// Quando i for igual a 10, ou seja, valor falso, o incremento para de ser executado
for (var i = 0; i < 10; i++){
    console.log('i = ', i)
}
console.log('fora bloco: i = ', i)





// decremento
for (var d = 5; d > -1; d--){
    console.log('d = ', d)
}
console.log('fora bloco: d = ', d)